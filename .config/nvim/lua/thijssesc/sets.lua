-- sets

-- global options
vim.opt.hidden = true           --
vim.opt.backup = false          --
vim.opt.writebackup = false     --
vim.opt.swapfile = false        --
vim.opt.showcmd = false         -- show incomplete cmds down the bottom
vim.opt.showmode = false        -- do not show current mode down the bottom
vim.opt.scrolloff = 4           -- give some space at the top and bottom
vim.opt.splitbelow = true       -- split below instead of on top
vim.opt.splitright = true       -- split to the right instead of the left

-- window options
vim.opt.cursorline = true      -- enable cursor line
vim.opt.number = true          -- enable line numbers
vim.opt.relativenumber = true  -- enable relative line numbers

-- indent options
vim.opt.shiftwidth = 2          -- change the number of space charaters inserted for indentation
vim.opt.tabstop = 2             -- insert 2 spaces for a tab
vim.opt.softtabstop = 2         --
vim.opt.expandtab = true        -- converts tabs to spaces
vim.opt.smartindent = true      --

